#RelationsOp.py
x=10
y=20

#1.==(Exact equal to)
print(x==10)
print(x=='10')

#2.!=(Not equal to)
print(x!=10)

#3.>(Greater than)
print(x>y)

#4.<(Less than)
print(x<y)

#5.>=(Greater than or equal to)
print(x>=10)
print(x>=y)

#6.<=(Less than or equal to)
print(x<=10)

print(x<=y)
